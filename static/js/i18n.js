function interpolateText(text, params) {
  if (!params || typeof params !== 'object') return text;
  return String(text).replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key) => (
    params[key] === undefined || params[key] === null ? '' : String(params[key])
  ));
}

function t(key, langOrParams, maybeParams) {
  let lang = currentLang;
  let params = {};
  if (typeof langOrParams === 'string') {
    lang = langOrParams;
    params = maybeParams || {};
  } else if (langOrParams && typeof langOrParams === 'object') {
    params = langOrParams;
  }
  const text = (translations[lang] && translations[lang][key]) || (translations.en && translations.en[key]) || key;
  return interpolateText(text, params);
}

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem('codex_gui_lang', lang);
  location.reload();
}

function applyI18n() {
  // data-i18n attributes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (text !== key) {
      if (el.tagName === 'INPUT' && el.placeholder) {
        el.placeholder = text;
      } else if (el.tagName === 'INPUT' && el.type !== 'hidden') {
        el.value = text;
      } else if (el.tagName === 'TITLE') {
        document.title = text;
      } else if (el.tagName === 'OPTION') {
        el.textContent = text;
      } else {
        el.textContent = text;
      }
    }
  });

  // data-i18n-placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const text = t(key);
    if (text !== key) {
      el.placeholder = text;
    }
  });
}

// 页面加载后自动应用
document.addEventListener('DOMContentLoaded', applyI18n);
